#include <iostream>
using namespace std;

int main() {

    int x = 18;

    if (x == 10){
        cout <<"I've got sunshine on a cloudy day";
    }
    else if (x > 10){
        cout <<"When it's cold outside, I've got the month of May";
    }
    else {
        cout <<"I guess you'd say, what can make me feel this way?";
    }

    return 0;
}

